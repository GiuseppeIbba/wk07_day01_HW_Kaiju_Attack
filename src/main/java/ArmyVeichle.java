public class ArmyVeichle {

    String type;
    int healthValue;

    public ArmyVeichle(String type, int healthValue) {
        this.type = type;
        this.healthValue = healthValue;
    }

    public String gettype() {
        return this.type;
    }

    public int getHealth() {
        return this.healthValue;
    }
}
